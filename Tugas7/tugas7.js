$("document").ready(function () {
    $("div.accordion-collapse, div.accordion-body, div.box-1, div.ayam").click(function () {
        $(this).css({
            background: "url('Images/Ayam.png')"
        });
    });
    $("div.accordion-collapse, div.accordion-body, div.box-1, div.ayam").click(function () {
        $("<audio></audio>").attr({
            'src': 'Audio/ayam.mpeg',
            'volume': 0.4,
            'autoplay': 'autoplay'
        }).appendTo("div");
    });
});
